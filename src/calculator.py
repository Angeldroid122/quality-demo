# СЛИШКОМ ДЛИННАЯ СТРОКА (больше 100 символов - ошибка линтера)
def add(a, b):
    """Возвращает сумму a и b."""
    return a + b

def subtract(a, b):
    """Возвращает разность a и b."""
    return a - b

def multiply(a, b):
    """Возвращает произведение a и b."""
    return a * b

def divide(a, b):
    """Возвращает частное a и b. Вызывает ошибку если b = 0."""
    if b == 0:
        raise ValueError("Нельзя делить на ноль!")
    return a / b

def power(a, b):
    """Возвращает a в степени b (эта функция без теста - покрытие упадёт)."""
    return a ** b
